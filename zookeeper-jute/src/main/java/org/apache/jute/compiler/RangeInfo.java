package org.apache.jute.compiler;

public abstract class RangeInfo implements Comparable<RangeInfo> {

    private int beginLine;
    private int endLine;
    private int beginColumn;
    private int endColumn;

    public int getBeginLine() {
        return beginLine;
    }

    public void setBeginLine(int beginLine) {
        this.beginLine = beginLine;
    }

    public int getEndLine() {
        return endLine;
    }

    public void setEndLine(int endLine) {
        this.endLine = endLine;
    }

    public int getBeginColumn() {
        return beginColumn;
    }

    public void setBeginColumn(int beginColumn) {
        this.beginColumn = beginColumn;
    }

    public int getEndColumn() {
        return endColumn;
    }

    public void setEndColumn(int endColumn) {
        this.endColumn = endColumn;
    }

    @Override
    public int compareTo(RangeInfo o) {
        int compare = Integer.compare(getBeginLine(), o.getBeginLine());
        if (compare != 0) {
            return compare;
        }

        return Integer.compare(getBeginColumn(), o.getBeginColumn());
    }
}
